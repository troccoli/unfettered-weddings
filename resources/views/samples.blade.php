                Originally, a wedding ring was given to the bride as a symbol of possession. Mandy would like to clearly
                state that this is NOT the case here.
            </p>
            <p>
                In more modern times, a wedding ring is a sign of a promise, and represents the love, honour and
                faithfulness that you have for your spouse.
                It is also a sign of the love you have for each other. A love that should last forever, symbolised by a
                ring that has no beginning nor end.
            </p>
            <p>
                Today, Mandy and Neil have chosen to exchange only one ring. They believe that marriage is one union
                which is better represented by one ring.
            </p>
