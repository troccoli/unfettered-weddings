            <p class="mb-4 font-bold underline underline-offset-8">On marriage</p>
            <p>
                &quot;Why do you want to get married?&quot;
            </p>
            <p>
                This is a question I often ask. Couples give me the most varied answers: love, of course, companionship,
                stability and security are only a few of those.
                But when I asked Mandy and Neil, their answer surprised me:
            </p>
            <p>
                &quot;To celebrate we have found happiness again&quot;.
            </p>
            <p>
                It's that word, &quot;again&quot;, that surprised me.
                You see, for many, marriage is a once-in-a-lifetime event.
                For some, it's a rite of passage.
                For Mandy and Neil, it's a statement.
            </p>
            <p>
                A statement of hope, of looking into the future with someone to share it with, of sharing that future
                with someone who loves you, cares for you and accepts you.
                It's also a statement to each other, to say how strong, confident and stable they feel because of the
                other person.
            </p>
            <p>
                And last but certainly not least, a statement to their children, to say how grateful they are to have
                been welcomed by them. They all now have an extra friend to rely on.
            </p>
