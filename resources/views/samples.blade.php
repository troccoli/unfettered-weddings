            <p class="mb-4 font-bold underline underline-offset-8">Couple's story</p>
            <p>
                Mandy and Neil like to say they are a modern couple. &quot;We met online, you know&quot;,
                Mandy loves to tell everyone. That's true, but it wasn't eHarmony or Tinder.
                The matter of fact is, Mandy needed a new conservatory, and Neil was listed on checkatrade.com.
                When Mandy decided to move to the Kent's countryside a few years ago, she did it to spend more time with
                Stephen, Susan and especially her new grandson, Thomas.
                After many fruitless viewing, she saw a small cottage, with a good size garden, and she instantly fell
                in love with it.
            </p>
            <p>
                The garden was South-facing so she wanted a conservatory from where she would enjoy the sun, and little
                Thomas, all day long.
                She didn't know any builders and none of her friends had any local recommendations. So she did what
                people do these days. She went online.
                Neil's name was at the top of the list, with very good reviews and a high rating. She gave him a call
                and the rest is history, as they say.
            </p>
            <p>
                I don't know about you, but I love history, so let's see what happened next.
            </p>
            <p>
                It was a rainy April afternoon when Neil went to Mandy's house for a quote.
                In spite of the weather, Neil insisted he wanted to go out into the garden to have a better look
                at the work needed.
                He came back inside soaking wet, and Mandy offered him a hot cup of tea to give him a chance to dry.
                They started talking and two more cups of tea later, they realised they had just spent almost two hours
                chatting.
            </p>
            <p>
                When, the day after, Neil came back with a quote, Mandy thought it reasonable and gave him the
                job.
                During the next four months, they talked a lot about the conservatory and a lot more about themselves.
                They found out they both like nature and long walks in the countryside, travelling to exotic places,
                and Benidorm, and drag races. No, I'm not talking about RuPaul's, I'm
                talking about the car racing type.
            </p>
            <p>
                The actual building work took three weeks. Neil confessed that he could have done it in two, but he
                enjoyed talking to Mandy so much during those visits that he took his time.
                On the very last day, in the new conservatory, he finally asked Mandy out.
                Two years later, on the same date, and in the very same conservatory, she asked Neil to move in.
                He thought about it for a moment, looked at her and said with a straight face:
                &quot;<span class="font-speech italic">Well, you do have a pretty conservatory</span>&quot;,
                before smiling and putting Mandy out of her misery.
            </p>
            <p>
                In the beginning, living together was not as smooth sailing as you may think, but they soon found their
                rhythm, their flow, their &quot;ecosystem&quot;, as Neil likes to call it.
                They realised how much of a good team they make,
                and at the same time how much they both retain their individuality. Signs of a strong relationship.
            </p>
            <p>
                If you know Mandy, you also know how much she wanted to see the Northern Lights.
                If you don't know Mandy, well, let me just say that she can spend entire afternoons talking about it,
                and how one day she will go and be fortunate enough to actually see them.
                Neil had experienced a few of these afternoons, so one day he decided that THAT would be where he would
                propose.
                They booked the holiday and off they went.
            </p>
            <p>
                Neil, would you believe it, had prepared a whole speech for when the Lights would finally appear, and
                how they would fade away in comparison to Mandy's beauty, etc. etc. etc.
                But it wasn't meant to be and he had to improvise.
            </p>
            <p>
                At the end of the holiday, when they realised they wouldn't see anything, Neil took the ring out of his
                pocket, held it high, and said
                &quot;<span class="font-speech italic">What's that?</span>&quot;.
                Mandy, still looking for the Northern Lights, didn't notice the ring and shouted
                &quot;<span class="font-speech italic">Where? Where?</span>&quot;.
                When it was clear that there were no lights she turned and saw Neil on his knees AND the ring in his
                hands.
                &quot;<span class="font-speech italic">Will you marry me?</span>&quot;
                She thought about it for a moment, looked at him and said with a straight face:
                &quot;<span class="font-speech italic">Well, you did build a pretty conservatory</span>&quot;,
                before smiling and putting Neil out of his misery.
            </p>
            <p>
                So there you have it. This is how they met and fell in love. If they're going to live happily ever after
                I do not know.
                But I know this. They have a very strong circle of friends and a welcoming family to support them.
            </p>
