            <x-section-title class="mb-6">The wedding of Mandy and Neil</x-section-title>
            <p>
                Neil and Mandy wanted a humanist ceremony as they wished to acknowledge how happy and stable their
                relationship had made them.
            </p>
            <p>
                They had both been married before, and have grown-up children. They didn't want them to feel
                uncomfortable, and they were anxious that none of their children felt that their other parent was in
                any way devalued.
            </p>
            <p>
                They wanted the ceremony to be fun and light hearted.
            </p>
            <p>
                This is a redacted version of the ceremony I wrote.
            </p>
